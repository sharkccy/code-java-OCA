package javaapplication1;

public class JavaApplication1 {
    public static void main(String[] args) {
       int a = 13 , b = 25;
        System.out.println(a & b);  // & 表示而且 
        
        System.out.println(a | b);  // | 表示或是
        
        System.out.println(a ^ b);  // ^ 表示XOR(互斥)
    }

}
